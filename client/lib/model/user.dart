class User{
  final int id;
  final String username;
  final String email;
  final String role;
  final String telephone;
  final String genre;
  final String plaque;
  final String type_voiture;
  final String photo_voiture;

  User({
    required this.id,
    required this.username,
    required this.email,
    required this.role,
    required this.telephone,
    required this.genre,
    required this.plaque,
    required this.type_voiture,
    required this.photo_voiture
  });
}